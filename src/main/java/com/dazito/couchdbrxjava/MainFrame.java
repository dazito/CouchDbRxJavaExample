    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(dbConnector != null) {
            List<Person> list = couchDBQuery.getAllPersons(dbConnector);

            StringBuilder stringBuilder = new StringBuilder();

            for (Person person : list) {
                createLine(stringBuilder, person);
            }

            jTextArea.append(stringBuilder.toString());
        }
        else {
            showErrorAlertDialog("Not connected to the server.");
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed


    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(dbConnector != null) {
            String firstName = firstNameTextField.getText();
            String lastName = lastNameTextField.getText();
            int age = Integer.parseInt(ageTextField.getText());
            String location = locationTextField.getText();
            String country = countryTextField.getText();

            Person person = new Person(firstName, lastName, age, location, country);

            couchDBQuery.addPerson(dbConnector, person);
        }
        else {
            showErrorAlertDialog("Not connected to the server.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed


    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(dbConnector != null) {
            ContinuousChangesFeed continuousChangesFeed = (ContinuousChangesFeed) couchDBQuery.changesFeed(myCouchDd.getDbConnector());

            subscription = continuousChangesFeed.onAdded()
                    .map(new Func1<StdDocumentChange, Person>() {
                        @Override
                        public Person call(StdDocumentChange stdDocumentChange) {
                            return parseDocumentChangeToPerson(stdDocumentChange);
                        }
                    })
                    .subscribe(new Action1<Person>() {
                        @Override
                        public void call(Person person) {
                            StringBuilder stringBuilder = new StringBuilder();
                            createLine(stringBuilder, person);
                            
                            jTextArea.append(stringBuilder.toString());
                        }
                    });
        }
        else {
            showErrorAlertDialog("Not connected to the server.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if(subscription != null) {
            if(!subscription.isUnsubscribed()) {
                subscription.unsubscribe();
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

