    private StringBuilder createLine(StringBuilder stringBuilder, Person person) {
        stringBuilder.append("Name: ");
        stringBuilder.append(person.getFirstName());
        stringBuilder.append(" ");
        stringBuilder.append(person.getLastName());
        stringBuilder.append(" :: Age: ");
        stringBuilder.append(person.getAge());
        stringBuilder.append(" :: Location: ");
        stringBuilder.append(person.getLocation());
        stringBuilder.append(" ::  Country: ");
        stringBuilder.append(person.getCountry());
        stringBuilder.append("\n");
        
        return stringBuilder;
    }
    
    private void showErrorAlertDialog(String message) {
        JOptionPane.showMessageDialog(this, message, ALERT_DIALOG_TITTLE, JOptionPane.ERROR_MESSAGE);
    }
    
    private Person parseDocumentChangeToPerson(StdDocumentChange stdDocumentChange) {
        JsonNode jsonNode = stdDocumentChange.getDocAsNode();
        String firstName = jsonNode.get("firstName").asText();
        String lastName = jsonNode.get("lastName").asText();
        String location = jsonNode.get("location").asText();
        String country = jsonNode.get("country").asText();
        int age = jsonNode.get("age").asInt(-1);

        return new Person(firstName, lastName, age, location, country);
    }


